#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (dest <= src)
	{
		while (i < n)
		{
			((unsigned char *)dest)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	else
	{
		while (i < n)
		{
			j = n - i - 1;
			((unsigned char *)dest)[j] = ((unsigned char *)src)[j];
			i++;
		}
	}
	return (dest);
}
