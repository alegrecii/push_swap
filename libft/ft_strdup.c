#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		size;
	char	*dest;

	size = 0;
	i = 0;
	while (src[size])
		size++;
	dest = malloc(size * sizeof (char) + 1);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
